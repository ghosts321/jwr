package com.wr4.domain;

public class IndicInfo {
	private String i; //第一列坐标
	private String j; //第二列坐标
	private String k; //第四列坐标
	private String name; //列 中显示的 text
	private String star; //星级
	private String score; //分数
	private String detaitype; //业务明细类型
	public String getI() {
		return i;
	}
	public void setI(String i) {
		this.i = i;
	}
	public String getJ() {
		return j;
	}
	public void setJ(String j) {
		this.j = j;
	}
	public String getK() {
		return k;
	}
	public void setK(String k) {
		this.k = k;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStar() {
		return star;
	}
	public void setStar(String star) {
		this.star = star;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getDetaitype() {
		return detaitype;
	}
	public void setDetaitype(String detaitype) {
		this.detaitype = detaitype;
	}
	
	
}
